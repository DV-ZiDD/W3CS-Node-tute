var adder = function add(a, b) {
    return a + b;
};

var subtractor = function sub(a, b) {
    return a - b;
};

var multiplier = function mul(a, b) {
    return a * b;
};

var devider = function dev(a, b) {
    return a / b;
};

var moduler = function mod(a, b) {
    return a % b;
};

exports.Adder = adder;
exports.Subtractor = subtractor;
exports.Multiplier = multiplier;
exports.Devider = devider;
exports.Moduler = moduler;